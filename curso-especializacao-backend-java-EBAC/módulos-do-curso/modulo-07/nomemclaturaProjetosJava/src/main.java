public class main {

    public static void main(String[] args) {

         /*➔Classes, Interfaces e Enum: A primeira letra deve ser maiúsculas, caso o nome seja formado por mais de uma palavra,
         as demais palavras devem ter sua primeira letra maiúscula.*/
        System.out.println("NomeDeUmaClasse");

         /*➔Métodos, Atributos e Variáveis: A primeira letra do método deve ser minúscula e após devemos aplicar o camelCase.*/
        System.out.println("variavelQualquer");

         /*➔Constantes: Todas as letras devem ser maiúsculas.*/
        System.out.println("NOME_DE_UMA_CONSTANTE");

    }

}
